					<p class="test-selected-review">Malgré quelques idées excellentes, Assassin's Creed 4 : Black Flag n'est certainement pas le meilleur opus de la série. Tout d'abord son scénario déçoit, tout comme son gameplay qui commence à vieillir sérieusement. Le système de free run, notamment, n'est pas du tout adapté aux environnements étriqués que sont les bateaux de pirates, tandis que l'IA des gardes est de moins en moins facile à avaler. Reste que l'ambiance proposée est tout simplement excellente, que l'univers est passionnant à explorer et très vaste, que la liberté offerte au joueur est totale, que l'immersion est enivrante et que le multi est plus riche que jamais. Bref, il s'agit d'un jeu tout en nuances, qui mérite tout de même le coup d'oeil, notamment si vous êtes un amateur de la série ou de l'univers de la piraterie.</p>
